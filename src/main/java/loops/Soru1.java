package loops;

public class Soru1 {
    public static void main(String[] args) {

        // 1'den 10a kadar sayilari ekrana yazdiran kodu yaziniz

        //1.YOL :
        int i = 1;

        do {
            System.out.print(i+" ");
            i++;
        }while(i<11);

        System.out.println();


        // 2.YOL
        for (int j = 1; j <11 ; j++) {
            System.out.print(j+" ");

        }

        System.out.println();

        int k = 0;

        while (k<10){
            k++;
            System.out.print(k+" ");
        }












































    }
}
